import { FlowChart } from './flow-chart';

export async function generateMetadata({ searchParams }: { searchParams: {[key: string]: string | undefined } }) {
    const title = searchParams['report'] ? `${searchParams['report']}` : 'Reports';
    const description = 'Reports generated by Scholar Maps';
    return { title, description };
}

export default function Reports() {
    return (
        <section className="bg-background absolute lg:left-[16.666%] flex flex-col gap-2 w-full overflow-x-hidden lg:w-5/6">
            <FlowChart variant="figma" />
        </section>
    )
}